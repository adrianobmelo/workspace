
public class LacoFor {
	// Desenvolvendo um novo método
	public static void main(String[] args) {
		// Criar uma variável
		int numero;
		
		// Iniciar a variáve com um valor
		numero = 0;
		
		//Criando um laço de repetição
		for(int contador=0;contador<100;contador++) {
			// Fiz com que a variável número receba um valor
			numero=contador;
			System.out.println(numero);
			
			
		}
		
		
		
		
		
		

	}

}
